
input = float(input("Enter a numeric value: "))

if input < 35:
    print("Fail")
elif 35 <= input < 60:
    print("Average")
elif 60 <= input < 75:
    print("Good")
elif 75 <= input < 90:
    print("Distinction")
elif input>100:
    print("Please Enter correct value")
else:
    print("|| सीता राम हनुमान ||")